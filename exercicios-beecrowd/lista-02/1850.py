#Soma, subtração, multiplicação, divisão, módulo, divisão inteira e potência

primeiroNumero = int(input())
segundoNumero = int(input())
print(primeiroNumero + segundoNumero)
print(primeiroNumero - segundoNumero)
print(primeiroNumero * segundoNumero)
